package inheritance.aggregation;

public class Item {
	String i_name;
	int price;

	public String getI_name() {
		return i_name;
	}

	public void setI_name(String i_name) {
		this.i_name = i_name;
	}

	public int getPrice() {
		return price;
	}

	public void setPrice(int price) {
		this.price = price;
	}

	public String toString() {
		return i_name + "" + price;
	}
}
